    private void mostrardatos() 
    {
        DefaultTableModel modelo=new DefaultTableModel();
        modelo.addColumn("N/C");
        modelo.addColumn("Nombre y apellido");
        modelo.addColumn("Direccion");
        modelo.addColumn("Gmail"); //Añadir columnas en la table para visualizar los datos
        modelo.addColumn("Telefono");
        modelo.addColumn("Estado civil");
        jTableDatos.setModel(modelo);
        String consultasql="select*from estudiantes"; //consulta de sql
        String data[]=new String[6];
        
        Statement st;
        try{
             st=cn.createStatement(); 
            ResultSet rs=st.executeQuery(consultasql); 
            while(rs.next()){ 
                data[0]=rs.getString(1);
                data[1]=rs.getString(2);
                data[2]=rs.getString(3);
                data[3]=rs.getString(4);
                data[4]=rs.getString(5);
                data[5]=rs.getString(6);
                modelo.addRow(data);
            }
        }catch (SQLException e){
            System.out.println("Error al mostrar Datos"+e);
    }
        }

    private void limpiarEntradas() //este metodo se activa una ves hacemos click en alguna parte del crud donde no haya informacion, entonces se limpian todas las cajas de texto y se prepara para hacer otro registro
    {
        txtNC.setText("");
        txtnombre.setText("");
        txtDireccion.setText("");
        txtGmail.setText("");
        txttelefono.setText("");
        txtComboEstado.setSelectedIndex(0); //Indice "0" significa seleccionar
    }
