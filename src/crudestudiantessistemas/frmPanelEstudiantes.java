        this.txtGmail.setText(this.jTableDatos.getValueAt(fila, 3).toString());
        this.txttelefono.setText(this.jTableDatos.getValueAt(fila, 4).toString());
        this.txtComboEstado.setSelectedItem(this.jTableDatos.getValueAt(fila, 5).toString());
